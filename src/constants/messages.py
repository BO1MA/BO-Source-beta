def decorate_text(text: str) -> str:
    """Apply Arabic decoration to text."""
    result = []
    for char in text:
        result.append(DECORATION_MAP.get(char, char))
    return "".join(result)


def contains_bad_word(text: str) -> bool:
    """Check if text contains profanity."""
    text_lower = text.strip()
    for word in BAD_WORDS:
        if word in text_lower:
            return True
    return False


def get_greeting_response(text: str) -> str | None:
    """Return a random response if text matches a greeting."""
    for trigger, responses in GREETING_RESPONSES.items():
        if trigger in text:
            return random.choice(responses)
    return None
